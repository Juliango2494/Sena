// EJERCICIO 2: DETERMINAR SI UN NUMERO ES POSITIVO O NEGATIVO
alert("EJERCICIO 2:")
let numero = Number(prompt("Ingrese un numero para ver si es positivo o negativo"))
if(numero > 0){
    alert("El numero es positivo")
}else if(numero == 0){
    alert("El 0 es neutro")
}
else{
    alert("El numero es negativo")
}